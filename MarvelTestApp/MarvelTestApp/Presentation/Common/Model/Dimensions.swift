import Foundation
import UIKit

struct Dimensions {
    
    struct Margin {
        static let xSmall: CGFloat = 4
        static let small: CGFloat = 8
        static let medium: CGFloat = 16
        static let large: CGFloat = 24
    }
    
    struct NegativeMargin {
        static let xSmall: CGFloat = -4
        static let small: CGFloat = -8
        static let medium: CGFloat = -16
        static let large: CGFloat = -24
    }
    
    static let zero: CGFloat = 0

}

